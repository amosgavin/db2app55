package com.asiainfo.httpserver.pojo;

public class AuditInfo {

	private String nextStepOps;
	private String nextStepDes;
	private String roleId;

	public String getNextStepOps() {
		return nextStepOps;
	}

	public void setNextStepOps(String nextStepOps) {
		this.nextStepOps = nextStepOps;
	}

	public String getNextStepDes() {
		return nextStepDes;
	}

	public void setNextStepDes(String nextStepDes) {
		this.nextStepDes = nextStepDes;
	}

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
}
